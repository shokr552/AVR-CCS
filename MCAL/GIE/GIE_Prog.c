void M_GIE_Void_GlobalInterruptEnable(void)
{
	SET_BIT(SREG_REG,7);
}
/*--------------------------------------------------------------------------------------------*/
/*   FUNCTION BODY   */
/*--------------------------------------------------------------------------------------------*/
void M_GIE_Void_GlobalInterruptDisable(void)
{
	CLR_BIT(SREG_REG,7);
}
